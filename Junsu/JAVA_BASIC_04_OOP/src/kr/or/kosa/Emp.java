package kr.or.kosa;

public class Emp {
	private int empno;
	private String empName;
	public Emp(int empno, String empName) {
		this.empno = empno;
		this.empName = empName;
	}
	public int getEmpno() {
		return empno;
	}
	public void setEmpno(int empno) {
		this.empno = empno;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
}